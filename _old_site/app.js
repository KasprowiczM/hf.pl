let currentLang = 'pl';
let chartInstances = {};

/* ===== LANGUAGE SWITCH ===== */
function setLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  
  // Update all [data-i18n] elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
  
  // Update SVG text elements with data-i18n-key
  document.querySelectorAll('[data-i18n-key]').forEach(el => {
    const key = el.getAttribute('data-i18n-key');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
  
  // Update lang buttons
  document.querySelectorAll('.nav__lang-btn').forEach(btn => {
    const isActive = btn.getAttribute('data-lang') === lang;
    btn.classList.toggle('active', isActive);
    btn.setAttribute('aria-pressed', isActive);
  });
  
  // Rebuild charts with new labels
  initCharts();
}

/* ===== THEME TOGGLE ===== */
function initTheme() {
  const root = document.documentElement;
  const toggle = document.querySelector('[data-theme-toggle]');
  // Default to dark for luxury branding; respect system preference only if light
  let theme = 'dark';
  root.setAttribute('data-theme', theme);
  updateThemeIcon(theme);
  
  if (toggle) {
    toggle.addEventListener('click', () => {
      theme = theme === 'dark' ? 'light' : 'dark';
      root.setAttribute('data-theme', theme);
      updateThemeIcon(theme);
      // Rebuild charts with updated colors
      initCharts();
    });
  }
}

function updateThemeIcon(theme) {
  const toggle = document.querySelector('[data-theme-toggle]');
  if (!toggle) return;
  const icon = toggle.querySelector('i, svg');
  if (icon) {
    // We'll re-create icon on theme change
    toggle.innerHTML = theme === 'dark'
      ? '<svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><circle cx="12" cy="12" r="5"/><path d="M12 1v2M12 21v2M4.22 4.22l1.42 1.42M18.36 18.36l1.42 1.42M1 12h2M21 12h2M4.22 19.78l1.42-1.42M18.36 5.64l1.42-1.42"/></svg>'
      : '<svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><path d="M21 12.79A9 9 0 1 1 11.21 3 7 7 0 0 0 21 12.79z"/></svg>';
  }
  toggle.setAttribute('aria-label', `Switch to ${theme === 'dark' ? 'light' : 'dark'} mode`);
}

