/* ===== COPY EMAIL ===== */
function initCopyEmail() {
  const emailLink = document.getElementById('emailLink');
  const toast = document.getElementById('copyToast');
  if (!emailLink || !toast) return;
  
  emailLink.addEventListener('click', (e) => {
    // Don't prevent default — let mailto work
    // But also copy to clipboard
    if (navigator.clipboard) {
      navigator.clipboard.writeText('domain@hf.pl');
      toast.textContent = translations[currentLang].copied;
      toast.classList.add('show');
      setTimeout(() => toast.classList.remove('show'), 2000);
    }
  });
}

/* ===== SMOOTH SCROLL ===== */
function initSmoothScroll() {
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', (e) => {
      const target = document.querySelector(anchor.getAttribute('href'));
      if (target) {
        e.preventDefault();
        target.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });
}

/* ===== NUMBER ANIMATION ===== */
function animateNumbers() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const el = entry.target;
        const text = el.textContent.trim();
        // Only animate pure numbers
        const num = parseInt(text.replace(/[^0-9]/g, ''));
        if (isNaN(num) || num === 0) return;
        
        const prefix = text.match(/^[^0-9]*/)?.[0] || '';
        const suffix = text.match(/[^0-9]*$/)?.[0] || '';
        
        let start = 0;
        const duration = 1200;
        const startTime = performance.now();
        
        function tick(now) {
          const elapsed = now - startTime;
          const progress = Math.min(elapsed / duration, 1);
          // easeOutQuart
          const ease = 1 - Math.pow(1 - progress, 4);
          const current = Math.floor(ease * num);
          el.textContent = prefix + current.toLocaleString() + suffix;
          if (progress < 1) requestAnimationFrame(tick);
          else el.textContent = text;
        }
        
        requestAnimationFrame(tick);
        observer.unobserve(el);
      }
    });
  }, { threshold: 0.5 });
  
  document.querySelectorAll('.stats-bar__value').forEach(el => observer.observe(el));
}

/* ===== SCROLL REVEAL ===== */
function initScrollReveal() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.08, rootMargin: '0px 0px -30px 0px' });
  
  // Group elements by parent section to stagger within each section
  const sections = new Map();
  document.querySelectorAll('.fade-in').forEach(el => {
    const parent = el.closest('section') || el.closest('.stats-bar') || el.parentElement;
    if (!sections.has(parent)) sections.set(parent, []);
    sections.get(parent).push(el);
  });
  
  sections.forEach(elements => {
    elements.forEach((el, i) => {
      el.style.transitionDelay = `${i * 60}ms`;
      observer.observe(el);
    });
  });
}

/* ===== INIT ===== */
document.addEventListener('DOMContentLoaded', () => {
  // Init Lucide icons
  if (typeof lucide !== 'undefined') {
    lucide.createIcons();
  }
  
  // Theme
  initTheme();
  
  // Language buttons
  document.querySelectorAll('.nav__lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      setLanguage(btn.getAttribute('data-lang'));
    });
  });
  
  // Charts — wait for Chart.js to load
  const chartInterval = setInterval(() => {
    if (typeof Chart !== 'undefined') {
      clearInterval(chartInterval);
      initCharts();
    }
  }, 100);
  
  // Other init
  initCopyEmail();
  initSmoothScroll();
  
  // Scroll reveal
  initScrollReveal();
  
  // Delay number animation slightly
  setTimeout(animateNumbers, 300);
});
